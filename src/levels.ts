function getLevel(lvlNumber: number) {
    let level: Level;
    if (lvlNumber === 0) {
        level = {
            lines: [
                {
                    start: { x: levels[0].ceilPoints[0][0], y: levels[0].ceilPoints[0][1] },
                    end: { x: levels[0].ceilPoints[1][0], y: levels[0].ceilPoints[1][1] }
                }
            ],
            enemySpawns: [
                { spawnTime: 3000, ai: 1, startY: 100 },
                { spawnTime: 6000, ai: 0, startY: 666 },
                { spawnTime: 9000, ai: 1, startY: 50 }
            ]
        };
        for (const i in levels[0].ceilPoints.slice(1)) {
            let prev = levels[0].ceilPoints[parseInt(i)];
            let now = levels[0].ceilPoints[parseInt(i) + 1];
            level.lines.push({
                start: { x: prev[0], y: prev[1] },
                end: { x: now[0], y: now[1] }
            })
        }
    }
    return level;
}

